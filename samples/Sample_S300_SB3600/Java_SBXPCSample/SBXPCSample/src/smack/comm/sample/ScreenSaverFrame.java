    private void btnSetSleepMessageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSetSleepMessageMouseClicked
        boolean ret;
        int errorCode;
        int glyphWidth, glyphHeight;
        
        lblMessage.setText("Working...");
        invalidate();

        ret = SBXPCProxy.EnableDevice(SysUtil.MachineNumber, false);

        if (!ret)
        {
            lblMessage.setText("No Device");
            return;
        }

        OneStringOutput output = SysUtil.MakeXMLCommandHeader("GetSleepMsgGlyphSize");

        output = SBXPCProxy.GeneralOperationXML(SysUtil.MachineNumber, output.value);
        if (output.isSuccess())
        {
            glyphWidth = SBXPCProxy.XML_ParseInt(output.value, "Width");
            glyphHeight = SBXPCProxy.XML_ParseInt(output.value, "Height");

            OneStringOutput fontXML = new OneStringOutput();
            fontXML.value = "";
            fontXML = SBXPCProxy.XML_AddString(fontXML.value, "FaceName", "Arial");
            fontXML = SBXPCProxy.XML_AddInt(fontXML.value, "Height", Integer.parseInt(txtFontHeight.getText()));
            fontXML = SBXPCProxy.XML_AddInt(fontXML.value, "Width", Integer.parseInt(txtFontWidth.getText()));
            fontXML = SBXPCProxy.XML_AddInt(fontXML.value, "Weight", Integer.parseInt(txtFontWeight.getText()));
            fontXML = SBXPCProxy.XML_AddBoolean(fontXML.value, "Italic", chkItalic.isSelected());
            fontXML = SBXPCProxy.XML_AddBoolean(fontXML.value, "Underline", chkUnderline.isSelected());
            fontXML = SBXPCProxy.XML_AddBoolean(fontXML.value, "StrikeOut", chkStrikeOut.isSelected());

            if (chkDebugOut.isSelected()) 
                fontXML = SBXPCProxy.XML_AddString(fontXML.value, "DebugOut", txtDebugOutFile.getText());
            
            String sleepMessage = txtSleepMessage.getText();

            output = SysUtil.MakeXMLCommandHeader("SetSleepMessage");
            output = SBXPCProxy.XML_AddBinaryGlyph(output.value, sleepMessage, glyphWidth, glyphHeight, fontXML.value);

            output = SBXPCProxy.GeneralOperationXML(SysUtil.MachineNumber, output.value);
            
            if (output.isSuccess())
                errorCode = 0;
            else
                errorCode = (int)SBXPCProxy.GetLastError(SysUtil.MachineNumber).dwValue;
        }else
            errorCode = (int)SBXPCProxy.GetLastError(SysUtil.MachineNumber).dwValue;

        lblMessage.setText(SysUtil.ErrorPrint(errorCode));

        ret = SBXPCProxy.EnableDevice(SysUtil.MachineNumber, true);
    }//GEN-LAST:event_btnSetSleepMessageMouseClicked

