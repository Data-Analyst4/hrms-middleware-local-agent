    private void btnGetCustomerInfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGetCustomerInfoMouseClicked
        boolean ret;
        int errorCode = 0;
        
        lblMessage.setText("Working...");
        invalidate();

        ret = SBXPCProxy.EnableDevice(SysUtil.MachineNumber, false);

        if (!ret)
        {
            lblMessage.setText("No Device");
            return;
        }

        OneStringOutput output = SysUtil.MakeXMLCommandHeader("GetCustomerInfo");

        output = SBXPCProxy.GeneralOperationXML(SysUtil.MachineNumber, output.value);
        if (output.isSuccess())
        {
            OneStringOutput parseOutput = SBXPCProxy.XML_ParseBinaryUnicode(output.value, "CustomerName", 128L);
            txtCustomerName.setText(parseOutput.value);
            parseOutput = SBXPCProxy.XML_ParseBinaryUnicode(output.value, "CompanyName", 128L);
            txtCompanyName.setText(parseOutput.value);
            errorCode = 0;
        }else {
            errorCode = (int)SBXPCProxy.GetLastError(SysUtil.MachineNumber).dwValue;
        }

        lblMessage.setText(SysUtil.ErrorPrint(errorCode));

        ret = SBXPCProxy.EnableDevice(SysUtil.MachineNumber, true);
    }//GEN-LAST:event_btnGetCustomerInfoMouseClicked

