    private void lstTModeListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstTModeListMouseClicked
        
        DbTMode tMode = (DbTMode)lstTModeList.getSelectedValue();
        if(tMode == null)
            return;
        
        Calendar calendar = Calendar.getInstance();
        calendar.set(Calendar.HOUR_OF_DAY, tMode.startHour);
        calendar.set(Calendar.MINUTE, tMode.startMinute);
        txtStart.setValue(calendar.getTime());
        
        calendar.set(Calendar.HOUR_OF_DAY, tMode.endHour);
        calendar.set(Calendar.MINUTE, tMode.endMinute);
        txtEnd.setValue(calendar.getTime());
        
        cmbTrStatus.setSelectedIndex(tMode.doorStatus);
}//GEN-LAST:event_lstTModeListMouseClicked

