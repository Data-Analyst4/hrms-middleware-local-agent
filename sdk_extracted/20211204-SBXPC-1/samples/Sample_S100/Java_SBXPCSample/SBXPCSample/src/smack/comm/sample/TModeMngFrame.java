    public class DbTMode
    {
        public final int index;
        
        public int startHour;
        public int startMinute;
        public int endHour;
        public int endMinute;
        public int doorStatus;
        
        public DbTMode(int index)
        {
            this.index = index;
            startHour = 0;
            startMinute = 0;
            endHour = 0;
            endMinute = 0;
            doorStatus = 0;
        }
        
        @Override
        public String toString()
        {
            NumberFormat formatter = new DecimalFormat("00");
            String ret;
            ret = "[No.] " + index + " ";
            ret += "[S] " + formatter.format(startHour) + ":" + formatter.format(startMinute) + " ";
            ret += "[E] " + formatter.format(endHour) + ":" + formatter.format(endMinute) + " ";
            ret += "[Status] " + cmbTrStatus.getItemAt(doorStatus);
            
            return ret;
        }
    }
    
    public class TModeListModel extends AbstractListModel
    {
        public static final int TMODE_COUNT = 10;
        public static final int ELEM_CNT = 5;
        
        DbTMode[] tModeList = new DbTMode[TMODE_COUNT];
        
        public TModeListModel()
        {
            for(int i = 0; i< TMODE_COUNT; i ++)
                tModeList[i] = new DbTMode(i);
        }
        
        public void setAllData(GetDeviceLongInfoOutput data)
        {
            if(data.value.length < TMODE_COUNT * ELEM_CNT)
                return;
            
            for(int i = 0; i < TMODE_COUNT; i ++)
            {
                tModeList[i].doorStatus      = data.value[i * ELEM_CNT + 0];
                tModeList[i].startHour     = data.value[i * ELEM_CNT + 1];
                tModeList[i].startMinute   = data.value[i * ELEM_CNT + 2];
                tModeList[i].endHour       = data.value[i * ELEM_CNT + 3];
                tModeList[i].endMinute     = data.value[i * ELEM_CNT + 4];
            }
            fireContentsChanged(this, 0, getSize());
        }
        
        public int[] getAllData()
        {
            int[] ret = new int[TMODE_COUNT * ELEM_CNT];
            
            int index;
            
            for(int i = 0; i < TMODE_COUNT; i ++)
            {
                index = i * ELEM_CNT;
                ret[index + 0] = tModeList[i].doorStatus;
                ret[index + 1] = tModeList[i].startHour;
                ret[index + 2] = tModeList[i].startMinute;
                ret[index + 3] = tModeList[i].endHour;
                ret[index + 4] = tModeList[i].endMinute;
            }
            
            return ret;
        }
        
        public void setElementAt(int index, DbTMode tMode)
        {
            if(index < 0 || index >= TMODE_COUNT)
                return;

            checkData(tMode);
            
            tModeList[index] = tMode;
            
            fireContentsChanged(this, index, index);
        }
        
        private boolean checkData(DbTMode tMode)
        {
            if(tMode.doorStatus < 0)
            {
                tMode.doorStatus = 0;
                return false;
            }
            if(tMode.doorStatus >= cmbTrStatus.getItemCount())
            {
                tMode.doorStatus = cmbTrStatus.getItemCount() - 1;
                return false;
            }
            
            return true;
        }
        
        public int getSize() {
            return TMODE_COUNT;
        }

        public Object getElementAt(int index) {
            return tModeList[index];
        }        
    }
    
