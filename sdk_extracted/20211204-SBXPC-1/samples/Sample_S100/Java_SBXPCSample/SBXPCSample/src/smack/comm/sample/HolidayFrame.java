    private void lstHolidayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstHolidayMouseClicked
        DbHoliday holiday = (DbHoliday)lstHoliday.getSelectedValue();
        if(holiday == null)
            return;
        
        Calendar calendar = Calendar.getInstance();
        calendar.set(Calendar.MONTH, holiday.month - 1);
        calendar.set(Calendar.DATE, holiday.day);
        txtHoliday.setValue(calendar.getTime());
        
        txtPeriod.setText(String.valueOf(holiday.period));
}//GEN-LAST:event_lstHolidayMouseClicked

