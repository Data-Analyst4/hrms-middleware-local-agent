    public class DbHoliday
    {
        public final int index;
        
        public int month;
        public int day;
        public int period;
        
        public DbHoliday(int index)
        {
            this.index = index;
            month = 1;
            day = 1;
            period = 0;
        }
        
        @Override
        public String toString()
        {
            NumberFormat formatter = new DecimalFormat("00");
            String ret;
            ret = "[No.] " + index + " ";
            ret += "[Month/Day] " + formatter.format(month) + ":" + formatter.format(day) + " ";
            ret += "[Period] " + period;
            
            return ret;
        }
    }
    
    public class TModeListModel extends AbstractListModel
    {
        public static final int HOLIDAY_COUNT = 256;
        public static final int ELEM_CNT = 3;
        
        DbHoliday[] holidayList = new DbHoliday[HOLIDAY_COUNT];
        
        public TModeListModel()
        {
            for(int i = 0; i< HOLIDAY_COUNT; i ++)
                holidayList[i] = new DbHoliday(i);
        }
        
        public void setAllData(GetDeviceLongInfoOutput data)
        {
            if(data.value.length < HOLIDAY_COUNT * ELEM_CNT)
                return;
            
            for(int i = 0; i < HOLIDAY_COUNT; i ++)
            {
                holidayList[i].month     = data.value[i * ELEM_CNT + 0];
                holidayList[i].day       = data.value[i * ELEM_CNT + 1];
                holidayList[i].period    = data.value[i * ELEM_CNT + 2];
            }
            fireContentsChanged(this, 0, getSize());
        }
        
        public int[] getAllData()
        {
            int[] ret = new int[HOLIDAY_COUNT * ELEM_CNT];
            
            int index;
            
            for(int i = 0; i < HOLIDAY_COUNT; i ++)
            {
                index = i * ELEM_CNT;
                ret[index + 0] = holidayList[i].month;
                ret[index + 1] = holidayList[i].day;
                ret[index + 2] = holidayList[i].period;
            }
            
            return ret;
        }
        
        public void setElementAt(int index, DbHoliday holiday)
        {
            if(index < 0 || index >= HOLIDAY_COUNT)
                return;

            holidayList[index] = holiday;
            
            fireContentsChanged(this, index, index);
        }
        
        public int getSize() {
            return HOLIDAY_COUNT;
        }

        public Object getElementAt(int index) {
            return holidayList[index];
        }        
    }
    
